import UIKit

extension UIFont {
    
    static func avenir18() -> UIFont? {
        return UIFont.init(name: "avenir", size: 18)
    }
    
    static func avenir26() -> UIFont? {
        return UIFont.init(name: "avenir", size: 26)
    }
    
}
